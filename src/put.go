func DetectType(data interface{}) string {

	d := reflect.ValueOf(data)
	switch d.Kind() {
	case reflect.Slice:
		return "Slice"
	case reflect.Map:
		return "Map"
	case reflect.Struct:
		return "Struct"
	case reflect.String:
		return "String"
	case reflect.Float64:
		return "Float64"
	case reflect.Int32:
		return "Int32"
	case reflect.Int64:
		return "Int64"
	case reflect.Bool:
		return "Bool"
	}
	return ""

}

func DetectArrowType(data interface{}) arrow.DataType {

	d := reflect.ValueOf(data)
	switch d.Kind() {
	case reflect.String:
		return arrow.BinaryTypes.String
	case reflect.Float64:
		return arrow.PrimitiveTypes.Float64
	case reflect.Int32:
		return arrow.PrimitiveTypes.Int32
	case reflect.Int64:
		return arrow.PrimitiveTypes.Int64
	case reflect.Bool:
		return arrow.FixedWidthTypes.Boolean
	case reflect.Map:
		return arrow.StructOf(jsonToArrow(data, nil)...)
	}
	return nil
}

func jsonToArrow(data interface{}, fields []arrow.Field) []arrow.Field {

	if DetectType(data) == "Map" {
		for key, value := range data.(map[string]interface{}) {
			fields = append(fields, arrow.Field{
				Name:     key,
				Type:     DetectArrowType(value),
				Metadata: arrow.Metadata{},
			})
		}
	}

	return fields
}

func createArrowFields(data interface{}) []arrow.Field {
	fields := make([]arrow.Field, 1)
	fields = jsonToArrow(data, fields)
	return fields

}

