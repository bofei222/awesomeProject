package map_test

import "testing"

func TestInitMap(t *testing.T) {
	m1 := map[int]int{1: 1, 2: 4, 3: 9}
	t.Log(m1[2])
	t.Log(len(m1))
	m2 := map[int]int{}
	m2[4] = 16
	t.Logf("len m2=%d", len(m2))
	m3 := make(map[int]int, 10) // 不必由{}
	t.Logf("len m3=%d", len(m3))

}

func TestAccessNotExistingKey(t *testing.T) {
	m1 := map[int]int{}
	t.Log(m1[1])
	m1[2] = 0
	t.Log(m1[2])
	// 是不存在呢 还是 本身是0值 用ok区分

	if v, ok := m1[2]; ok {
		t.Log(m1[3])
		t.Log(v)
	} else {
		t.Log("not ex")
	}
}

